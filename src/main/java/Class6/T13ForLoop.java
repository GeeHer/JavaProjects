package Class6;

public class T13ForLoop {
    public static void main(String[] args) {
        for (int i = 100; i >=1; i--) {
            if(i%2!=0)
            System.out.print(i+" ");

        }
        System.out.println();
        int num=99;
        while(num>=1){
            System.out.print(num+" ");
            num-=2;

        }
    }
}
