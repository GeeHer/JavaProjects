package Class2;

public class E8StringConcat {
    public static void main(String[] args) {

        int noOfSlices=11;
        int noOfFriends=3;
        System.out.println(noOfSlices/noOfFriends);
        System.out.println(noOfSlices%noOfFriends);

        String msg="My age is ";
        int age=20;
        String results=msg+age;
        System.out.println(results);
        System.out.println("A string"+(10+10));
    }
}
