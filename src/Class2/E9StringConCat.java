package Class2;

public class E9StringConCat {
    public static void main(String[] args) {
        //() /* +-
        System.out.println(2+2*2);
        System.out.println((2+2)*2);
        System.out.println("10"+10);
        System.out.println("10"+10+10);
        System.out.println("10"+(10+10));
        System.out.println("10"+10*10);

    }


}
