package Class2;

public class T3Variables {
    public static void main(String[] args) {

        double num1=5;
        double num2=6;
        double total=num1+num2;
        double subtraction=num1-num2;
        double multiplication=num1*num2;
        double division=num1/num2;


        System.out.println("The addition of two numbers " +num1+ " and "+num2+ " is equal to "+total);
        System.out.println("The subtraction of two numbers "+num1+" and "+num2+" is equal to "+subtraction);
        System.out.println("The multiplication of two numbers "+num1+" and "+num2+" is equal to "+multiplication);
        System.out.println("The division of two numbers "+num1+" and "+num2+" is equal to "+division);

        double square=3.9;
        double results=square*square;
        System.out.println("The square of "+square+" is "+results);

    }


}
